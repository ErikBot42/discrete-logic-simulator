macro_rules! unwrap_or_else {
    ($expression:expr, $block:expr) => {
        match $expression {
            Some(value) => value,
            _ => $block,
        }
    };
}

/// Assume in release, assert in debug.
macro_rules! assume_debug_assert {
    ($expression:expr) => {
        debug_assert!($expression);
        unsafe { std::intrinsics::assume($expression) };
    };
}

